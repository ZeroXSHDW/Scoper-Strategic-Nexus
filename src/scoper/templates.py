def get_template_info(template_name: str) -> dict:
    """Get template metadata including required variables."""
    content = get_template(template_name)
    if not content:
        return {}

    variables = re.findall(r'\{\{(\w+)\}\}', content)
    variables = list(set(variables))
    
    schema = get_template_schema(template_name)

    return {
        "name": template_name,
        "variables": variables,
        "variable_count": len(variables),
        "schema": schema
    }


def copy_template(source: str, dest: str) -> None:
    """Copy a template to a destination."""
    content = get_template(source)
    if not content:
        raise ValueError(f"Template '{source}' not found")

    Path(dest).write_text(content)


