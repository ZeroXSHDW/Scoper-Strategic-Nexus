"""SOW Templates Module - Supports built-in and user-defined templates."""

import re
from pathlib import Path
from typing import Any, Dict, List, Optional

import yaml

BUILTIN_TEMPLATE_DIR = Path(__file__).parent / "templates"
USER_TEMPLATE_DIR = Path.home() / ".config" / "scoper" / "templates"


def list_available_templates() -> List[str]:
    """List all available SOW templates from all sources."""
    templates = ["default", "standard", "simple"]

    for dir_path in [USER_TEMPLATE_DIR, BUILTIN_TEMPLATE_DIR]:
        if dir_path.exists():
            for f in dir_path.glob("*"):
                if f.suffix in [".md", ".jinja2", ".txt", ".html"] and f.stem not in templates:
                    templates.append(f.stem)

    return templates


def get_template_path(template_name: str) -> Optional[Path]:
    """Get the file path for a template."""
    # Check User directory first
    if USER_TEMPLATE_DIR.exists():
        for ext in [".md", ".jinja2", ".txt", ".html"]:
            tp = USER_TEMPLATE_DIR / f"{template_name}{ext}"
            if tp.exists():
                return tp

    # Check BUILTIN_TEMPLATE_DIR
    if BUILTIN_TEMPLATE_DIR.exists():
        for ext in [".md", ".jinja2", ".txt", ".html"]:
            tp = BUILTIN_TEMPLATE_DIR / f"{template_name}{ext}"
            if tp.exists():
                return tp

    return None


def get_template(template_name: str) -> Optional[str]:
    """Get template content by name."""
    if template_name in ["default", "standard", "simple"]:
        return get_builtin_template(template_name)

    path = get_template_path(template_name)
    return path.read_text() if path else None


def get_template_schema(template_name: str) -> Dict[str, Any]:
    """Get variable schema for a template if it exists."""
    # Built-in schemas could be added here if needed
    
    path = get_template_path(template_name)
    if path:
        schema_path = path.with_suffix(".vars.yaml")
        if schema_path.exists():
            try:
                return yaml.safe_load(schema_path.read_text()) or {}
            except Exception:
                return {}
    return {}


